/// description 문자열을 받아서, 사전에 정의된 키워드가 포함되면 매핑된 카테고리 반환.
/// 대소문자 구분 없이 부분 일치 검사.
///
/// 예:
///   mapCategory("카페베네 산본역점에서 결제")  → "식비"
///   mapCategory("토스 이준환")             → "이체"
///   mapCategory("알 수 없는 내용")         → "기타"
///
String mapCategory(String? description) {
  if (description == null || description.isEmpty) return "기타";

  final descLower = description.toLowerCase();
  for (final entry in _keywordToCategory.entries) {
    if (descLower.contains(entry.key.toLowerCase())) {
      return entry.value;
    }
  }
  return "기타";
}
